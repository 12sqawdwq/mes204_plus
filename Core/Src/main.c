/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  LCD_Init_Extreme();

  typedef enum {
    SCENE_3D_CUBE,
    SCENE_ASCII_VIDEO
} DemoState;

  DemoState current_state = SCENE_3D_CUBE;

  // 3D 场景变量
  float angle = 0.0f;
  int cube_frame_counter = 0;
  const int CUBE_DURATION_FRAMES = 400; // 3D 展示持续多久

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  while (1)
  {
    if (current_state == SCENE_3D_CUBE) {
      // ===========================
      // 场景 A: 3D 旋转立方体
      // ===========================

      // 1. 逻辑计算 (FPU 肌肉)
      GFX_Clear();
      GFX_Render_Cube(angle, angle * 1.5f, angle * 0.5f);
      angle += 0.15f; // 旋转速度

      // 2. 物理刷新 (CGRAM 动态映射)
      LCD_Refresh_Kaleidoscope();

      // 3. 状态切换检查
      cube_frame_counter++;
      if (cube_frame_counter >= CUBE_DURATION_FRAMES) {
        // 切换到视频模式前，最好清一下屏
        LCD_SendCommand(0x01);
        HAL_Delay(2); // 清屏指令慢

        Video_Reset(); // 重置视频进度
        current_state = SCENE_ASCII_VIDEO;
      }

      // 3D 模式帧率控制 (太快了看不清)
      HAL_Delay(30);
    }
    else if (current_state == SCENE_ASCII_VIDEO) {
      // ===========================
      // 场景 B: CGRAM 视频流
      // ===========================

      // 【新增】如果是刚进入该状态的第一帧，先刷背景
      static uint8_t video_initialized = 0;
      if (video_initialized == 0) {
        Video_Init_Screen(); // 刷入 0123... 克隆背景
        video_initialized = 1;
      }

      // 1. 刷新一帧 (更新 CGRAM)
      uint8_t is_playing = Video_Update_Frame();

      // 2. 状态切换检查
      if (is_playing == 0) {
        // 播放完毕
        LCD_SendCommand(0x01); // 清屏
        HAL_Delay(2);

        cube_frame_counter = 0;
        video_initialized = 0; // 重置标志位
        current_state = SCENE_3D_CUBE;
      }

      // 帧率控制
      HAL_Delay(40); // 调整速度
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

